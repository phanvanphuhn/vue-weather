<script>
export default {
  name: "app",
  data() {
    return {
      api_key: "d43e26ae27b3153b864ac57c3ddb96ec",
      url_base: "https://api.openweathermap.org/data/2.5/",
      query: "",
      weather: {},
    };
  },
  methods: {
    fetchWeather(e) {
      if (e.key === "Enter") {
        fetch(
          `${this.url_base}weather?q=${this.query}&units=metric&APPID=${this.api_key}`
        )
          .then((res) => {
            return res.json();
          })
          .then(this.setResults)
          .then(this.setDate);
      }
    },
    setResults(results) {
      this.weather = results;
    },
    setDate(results) {
      this.weather = results;
    },
  },
  computed: {},
  watch: {},
};
</script>

<template>
  <div id="app">
    <main>
      <div class="search-box">
        <input
          type="text"
          class="search-bar"
          placeholder="Search..."
          v-model="query"
          @keypress="fetchWeather"
        />
      </div>

      <div class="weather-container" v-if="typeof weather.main != 'undefined'">
        <div class="location-box">
          <div class="location">
            {{ weather?.name }}
          </div>
          <div class="date">2021</div>
        </div>

        <div class="weather-box">
          <div class="temp">{{ weather.main?.temp }}*C</div>
          <div class="weather">
            {{ weather?.weather[0]?.main }}
          </div>
        </div>
      </div>
    </main>
  </div>
</template>

